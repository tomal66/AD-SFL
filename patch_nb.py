    "             f'malicious fraction={MALICIOUS_FRACTION}')\n",
    "\n",
    "# Test accuracy & ASR\n",
    "ax = axes[0, 0]\n",
    "ax.plot(rounds_axis, adsfl_test_acc, marker='o', markersize=3, label='Test Accuracy')\n",
